package com.tmate.user.data;

public class FriendData {
    private String tv_name;
    private String tv_phone;
    private String tv_flag;

    public String getTv_name() {
        return tv_name;
    }

    public void setTv_name(String tv_name) {
        this.tv_name = tv_name;
    }

    public String getTv_phone() {
        return tv_phone;
    }

    public void setTv_phone(String tv_phone) {
        this.tv_phone = tv_phone;
    }

    public String getTv_flag() {
        return tv_flag;
    }

    public void setTv_flag(String tv_flag) {
        this.tv_flag = tv_flag;
    }
}
