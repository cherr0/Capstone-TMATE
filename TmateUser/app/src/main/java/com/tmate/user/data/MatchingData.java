package com.tmate.user.data;

public class MatchingData {
    private int iv_profile;
    private String tv_matching_name;
    private String tv_personnel;
    private String tv_start;
    private String tv_end;
    private String tv_start_place;
    private String tv_end_place;


    public MatchingData() {
        this.iv_profile = iv_profile;
        this.tv_matching_name = tv_matching_name;
        this.tv_personnel = tv_personnel;
        this.tv_start = tv_start;
        this.tv_end = tv_end;
        this.tv_start_place = tv_start_place;
        this.tv_end_place = tv_end_place;
    }

    public int getIv_profile() {
        return iv_profile;
    }

    public void setIv_profile(int iv_profile) {
        this.iv_profile = iv_profile;
    }

    public String getTv_matching_name() {
        return tv_matching_name;
    }

    public void setTv_matching_name(String tv_matching_name) {
        this.tv_matching_name = tv_matching_name;
    }

    public String getTv_personnel() {
        return tv_personnel;
    }

    public void setTv_personnel(String tv_personnel) {
        this.tv_personnel = tv_personnel;
    }

    public String getTv_start() {
        return tv_start;
    }

    public void setTv_start(String tv_start) {
        this.tv_start = tv_start;
    }

    public String getTv_end() {
        return tv_end;
    }

    public void setTv_end(String tv_end) {
        this.tv_end = tv_end;
    }

    public String getTv_start_place() {
        return tv_start_place;
    }

    public void setTv_start_place(String tv_start_place) {
        this.tv_start_place = tv_start_place;
    }

    public String getTv_end_place() {
        return tv_end_place;
    }

    public void setTv_end_place(String tv_end_place) {
        this.tv_end_place = tv_end_place;
    }
}
