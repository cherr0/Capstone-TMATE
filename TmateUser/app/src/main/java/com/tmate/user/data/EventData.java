package com.tmate.user.data;

public class EventData {
    private int iv_event;
    private String tv_title;
    private String tv_subTitle;
    private String tv_date;
    private String tv_bd_id;

    public int getIv_event() {
        return iv_event;
    }

    public void setIv_event(int iv_event) {
        this.iv_event = iv_event;
    }

    public String getTv_title() {
        return tv_title;
    }

    public void setTv_title(String tv_title) {
        this.tv_title = tv_title;
    }

    public String getTv_subTitle() {
        return tv_subTitle;
    }

    public void setTv_subTitle(String tv_subTitle) {
        this.tv_subTitle = tv_subTitle;
    }

    public String getTv_date() {
        return tv_date;
    }

    public void setTv_date(String tv_date) {
        this.tv_date = tv_date;
    }

    public String getTv_bd_id() {
        return tv_bd_id;
    }

    public void setTv_bd_id(String tv_bd_id) {
        this.tv_bd_id = tv_bd_id;
    }
}
